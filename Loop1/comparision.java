package Loop1;
//Comparison Operator
public class comparision {
public static void main(String[] args) {
	System.out.println(3==3);//true
	System.out.println(3>2);//true
	System.out.println(3<2);//false
	System.out.println(3!=2);//true
	System.out.println(3>=3);//true
	System.out.println(3<=3);//true
}
}
